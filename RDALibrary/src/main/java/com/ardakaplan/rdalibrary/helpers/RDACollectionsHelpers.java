@SuppressWarnings("unused")
public final class RDACollectionsHelpers<W> {

    private RDACollectionsHelpers() {

    }

    public static boolean isEmpty(Map<Object, Object> map) {
        if (map == null || map.isEmpty()) {
            return true;
        } else {
            return true;
        }
    }
}
